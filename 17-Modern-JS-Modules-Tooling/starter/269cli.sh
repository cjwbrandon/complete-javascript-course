# Video 269: A Brief Introduction to the Command Line
# Note: Always in a folder
ls # Shows content
cd # change directory
cd .. # change directory to parent - going up
cd ../.. # change 2 levels up
cd {folderName} # change directory to specific folder/child
cd {folderName}/{folderName}
clear # clear stdout

mkdir # Create folder/directory
touch {fileName}.{type} # Create file
touch {fileName1}.{type1} {fileName2}.{typ2} {fileName2}.{type2} # Creating multiple files
rm {fileName}.{type} # delete file
rm -R {folder} # deletes all files and folder
rmdir {folderName} # delete folder -> must be empty

mv {fileName}.{type} {location}/{newNameOptional} # moving a file